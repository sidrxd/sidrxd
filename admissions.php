<h2 class="team-details__title">Procedure for Admission to class Below II :-</h2>
<p>
1. Eligibility for Class I: - 5+ yrs as on 31 March, of the Academic Year of which Admission is Required.
<br>2. Advertisement for admission is published in leading newspapers during Sept. /Oct.
<br>3. Download application form. Click here to download Link.
<br>4. Read all instructions given on the page carefully.
<br>5. The duly filled form is to be submitted at the school within 15 days of form submission (or as mentioned on the website at that time) along with photocopies of latest marksheet, birth certificate and other certificates of achievements in academics and extra curriculars.
<br>6. After form is submitted, applicants are called for entrance test, the syllabus for which is given at the time of form submission. Parent interaction takes place on the same day, after the entrance.
<br>7. All orginal documents are to be brought on the day of interaction.
Results are usually declared within a week of entrance test/interaction.
<p>
<h2 class="team-details__title">Registration procedure and rules for class II and above :-</h2>
<p>
1. Registration Forms are to be filled in and submitted to the school office before the end of the registration period.
<br>2. Incomplete or illegible Registration Forms, without photographs will not be processed / accepted.
<br>3. Attested copy of Birth Certificate issued by Municipal Corporation or concerned civic authority, Address proof(Election card/ Aadhar card/ Telephone bill/ Electricity bill- any one of them) and Photocopy of Report Card of the last exam passed must be attached with the Registration Form
<br>4. Mid-Term admissions are possible depending on the vacancies arising in any given class.

<p>
<h2 class="team-details__title">Procedure for fee payment :-</h2>

1. Fee is charged quarterly for every student.
<p>
2. Fee at the time of admission is payable in the school office.
<br>
3. The name of your ward, class and registration no. should be written at the back of the Demand Draft / Payorder.
<br>
4. Fee, thereafter, is payable by A/C payee cheque or in cash at the bank extension counter only.
<br>
5. The name of the child / class / section / admission no. should be written at the back of the cheque and pay-in-slip.
<br>6. Those using the bus must ensure that the correct amount is filled in the fee cheque. Quarterly fee is due for payment on 1st April, 1st July, 1st October and 1st January of every quarter. Last date of payment is 10th of the first month of every quarter. Late payment fine is imposed thereafter till the last day of the month. The name of the child is liable to be struck off the rolls, if the fee is not paid by the last date of the said months.
</p>

Note: Only the Registration Form should be submitted at the time of registration. Admission Form and other forms are required only after the child is granted admission. For further admission queries, contact us:
